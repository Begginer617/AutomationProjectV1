package JavaArrays;

public class JavaArraysString {
    public static void main(String[] args) {
     String[]words;
     words = new String[20];
     words[0]="Sraka";
     words[1]="kaka";
     words[2]="ptaka";

      for (int i=0; i< words.length; i++){
            System.out.println(words[i]);
        }
    }
}
